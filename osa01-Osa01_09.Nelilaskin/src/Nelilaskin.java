
public class Nelilaskin {

    public static void main(String[] args) {
        int eka = 6;
        int toka = 3;
        double a = (double)eka / toka;

        System.out.println(eka + " + " + toka + " = " + (eka + toka));
        System.out.println(eka + " - " + toka + " = " + (eka - toka));
        System.out.println(eka + " * " + toka + " = " + (eka * toka));
        System.out.println(eka + " / " + toka + " = " + a);

        // Toteuta loput nelilaskimesta tähän.
    }
}
